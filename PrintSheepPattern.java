package CS1301Lab;
public class PrintSheepPattern {
    public static void main(String[] args)
    {
        System.out.println("         '__' ");
        System.out.println("         (oo)");
        System.out.println(" /=======// ");
        System.out.println(" / ||@@||");
        System.out.println(" * ||--||");
        System.out.println("   vv  vv");
        System.out.println("   ''  ''");
    }
}